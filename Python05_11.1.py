#!/usr/bin/env python3

A = {3,14,15,9,26,5,36,9}
B = {60,22,14,0,9}
print('Conjunto A =',A)
print('Conjunto B =',B,'\n')

print('Intersecao entre A e B =',A&B,'\n')

print('Diferenca A - B =',A-B)
print('Diferenca B - A =',B-A,'\n')

print('Uniao entre A e B =',A|B,'\n')

print('Diferenca simetrica entre A e B =',A^B)
